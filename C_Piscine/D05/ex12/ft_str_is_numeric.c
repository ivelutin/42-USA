int		ft_str_is_numeric(char *str)
{
	int proof;
	int i;

	proof = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
		{
			proof++;
		}
		i++;
	}
	if (proof > 0)
		return (0);
	else
		return (1);
}
