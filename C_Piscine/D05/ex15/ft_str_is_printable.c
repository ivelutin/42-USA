int		ft_str_is_printable(char *str)
{
	int i;
	int j;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] <= '~' && str[i] >= ' ')
		{
			if (str[i] >= '[' && str[i] <= '`')
			{
				i++;
				j = 0;
			}
			else
			{
				j = 1;
				i++;
			}
		}
		else
			return (0);
	}
	if (str[i] == '\0')
		return (1);
	return (0);
}
